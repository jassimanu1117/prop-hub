    // Store product + multiple images
    public function store(Request $request)
    {
        $data = $request->validate([
            'name' => 'required|string|max:255|unique:products,name',
            'description' => 'nullable|string',
            'price' => 'required|numeric',
            'category_id' => 'required|exists:categories,id',
            'brand_id' => 'required|exists:brands,id',
            'gender' => 'required|string|max:255',
            // ⭐ Sizes validation
            'sizes' => 'required|array|min:1',
            'sizes.*' => 'exists:sizes,id',
            //✅ Validate the array itself
            'images' => 'required|array|min:1',
            //✅ Validate each file inside the array
           'images.*' => 'image|mimes:jpeg,png,jpg,gif|max:2048',
           // Flags validation (optional, boolean)
           'is_trending' => 'nullable|boolean',
           'is_new_arrival' => 'nullable|boolean',
           'status' => 'required|in:active,inactive',
        ]);

        // Set default 0 if checkbox is not selected
        $data['is_trending'] = $request->has('is_trending') ? 1 : 0;
        $data['is_new_arrival'] = $request->has('is_new_arrival') ? 1 : 0;
     
        // Create product slug auto-generated by model
        $product = Product::create($data);

        // ⭐ Save Multiple Sizes
       // Sync sizes
       $product->sizes()->sync($request->sizes);

        // Handle multiple images
        if ($request->hasFile('images')) {
            foreach ($request->file('images') as $file) {
                $upload = uploadImage($file, 'products', true, [300, 300]);
                ProductImage::create([
                    'product_id' => $product->id,
                    'image_path' => $upload['image'],
                    'image_thumb' => $upload['thumbnail']
                ]);
            }
        }

        return redirect()->route('admin.products.index')->with('success', 'Product added successfully!');
    }

